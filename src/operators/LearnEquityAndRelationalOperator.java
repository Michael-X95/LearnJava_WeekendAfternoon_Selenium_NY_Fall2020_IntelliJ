package operators;

public class LearnEquityAndRelationalOperator {

    //EqualityAndRelationalOperator

    // == Equal to
    // != not Equal
    // > Greater than
    // >= Greater than Equal
    // < less than
    // <= less than Equal

    public static void main(String[] args) {
        int num1 = 50;
        if (num1 == 51) {
            //If condition is true then execute if block of code
            System.out.println("Number1 value is equal to 50");
        }
        if (num1 != 51) {
            //If condition is true then execute if block of code
            System.out.println("Number1 value is not equal to 50");
        }
        if (num1 > 51) {
            //If condition is true then execute if block of code
            System.out.println("Number1 value is greater than 50");
        }
        if (num1 >= 50) {
            //If condition is true then execute if block of code
            System.out.println("Number1 value is greater than 50");

        }
    }
}
